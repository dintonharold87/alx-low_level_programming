#include "3-calc.h"
/**
 * op_add-A function that adds  two integers
 * @a:first integer
 * @b:second integer
 * Return:the result
*/
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub-A function that gets the difference between two integers
 * @a:first integer
 * @b:second integer
 * Return:the result
*/
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul-A function that multiplies two integers
 * @a:first integer
 * @b:second integer
 * Return:the result
*/
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div-A function that divide two integers
 * @a:first integer
 * @b:second integer
 * Return:the result
*/
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod-A function that gets the remainder of the integer division
 * @a:first integer
 * @b:second integer
 * Return:the result
*/
int op_mod(int a, int b)
{
	return (a % b);
}
